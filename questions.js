function parseQuestionSource(source) {
  const questions = [];
  let category = 'General';
  const counters = {};
  source.split(/\r?\n/).forEach(rawLine => {
    const line = rawLine.trim();
    if (!line || line.startsWith('//')) return;
    if (line.startsWith('#')) {
      category = line.replace(/^#+\s*/, '').trim();
      return;
    }
    const parts = line.split('| Options:');
    const text = parts[0].trim();
    const options = parts[1] ? parts[1].split(';').map(item => item.trim()).filter(Boolean) : [];
    const slug = category.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
    counters[slug] = (counters[slug] || 0) + 1;
    questions.push({
      id: `${slug}-${String(counters[slug]).padStart(3, '0')}`,
      category,
      text,
      type: options.length ? 'choice-text' : 'text',
      options,
    });
  });
  return questions;
}

const QUESTIONS = parseQuestionSource(QUESTION_SOURCE);
